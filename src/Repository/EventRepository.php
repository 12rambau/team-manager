    /**
     * @return events[] Returns an array of 10 blogPosts
     */
    public function findTen(int $offset)
    {
        $events = $this->createQueryBuilder('b')
            ->orderBy('b.start', 'DESC')
            ->setFirstResult($offset)
            ->setMaxResults(10)
            ->getQuery()
            ->getResult();
            
        if ($events == null)
            $events = [];

        return $events;

    }

